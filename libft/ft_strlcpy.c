#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	j;

	j = 0;
	if (dstsize == 0)
		return (ft_strlen((char *)src));
	if (dstsize > 0)
	{
		while (src[j] != '\0' && j < dstsize - 1)
		{
			dst[j] = src[j];
			j++;
		}
		dst[j] = '\0';
	}
	return (ft_strlen((char *)src));
}
